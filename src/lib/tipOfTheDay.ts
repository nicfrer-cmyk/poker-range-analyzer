/** Deterministically picks one tip per calendar day, so the tip stays stable across
 *  reloads within the same day but changes day to day. Reuses the same seeded-RNG /
 *  date-key convention as the rest of the coach modules (see coachStorage.ts). */
export function getTipOfTheDay(): string {
  const rng = seededRng(`tip:${dateKey()}`);
  const index = Math.floor(rng() * TIPS.length);
  return TIPS[index] ?? TIPS[0]!;
}
